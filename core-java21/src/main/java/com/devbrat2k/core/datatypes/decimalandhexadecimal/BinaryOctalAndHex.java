package com.devbrat2k.core.datatypes.decimalandhexadecimal;

public class BinaryOctalAndHex {

    public static void main(String[] args) {

        int number1 = 0X10D;

        System.out.println(number1);

        int number2 = 0B11010;

        System.out.println(number2);

        Integer number3 = 01560;

        System.out.println(number3);

    }

}
